package variable;

public class Var2 {

  public static void main(String[] args) {
    // 값을 보관하고 꺼내쓸 수 있는 저장소인 "변수"를 제공
    // 변수 a를 10으로 초기화
    // 값을 바꾸려면 변수 a만 변경하면 된다
    int a = 10;

    System.out.println(a); // a의 값인 10으로 변경, 숫자 10 출력
    System.out.println(a);
    System.out.println(a);
  }
}
